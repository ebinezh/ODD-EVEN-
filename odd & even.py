# to print even numbers
n=int(input("Enter number: "))
x=[i for i in range(1, n+1) if not i% 2]
print("Even numbers till",n, "are", x)


# to print odd numbers
n=int(input("Enter number: "))
x=[i for i in range(1, n+1) if i% 2]
print("Odd numbers till",n, "are", x)

